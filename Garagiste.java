public class Garagiste {
		private String nom ;
		private String adresse ;
		
		

public Garagiste( String nom , String adresse ){
	this.nom = nom ;
	this.adresse = adresse ; 
	
}

public String getNom() {
	return nom ;
}
public String getAdresse(){
	return adresse ;
}	

public void setnom(String nom){
	this.nom = nom ;
}

}
